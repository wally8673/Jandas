package jandas.operaciones.ordenamiento;

/**
 * Enum que representa las posibles direcciones para el ordenamiento de datos.
 * <p>
 * Define si el ordenamiento es ascendente o descendente.
 * </p>
 */
public enum Orden {
    /**
     * Orden ascendente: de menor a mayor.
     */
    ASCENDENTE,

    /**
     * Orden descendente: de mayor a menor.
     */
    DESCENDENTE
}

